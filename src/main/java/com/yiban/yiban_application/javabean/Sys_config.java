package com.yiban.yiban_application.javabean;

public class Sys_config {

    private int config_id;
    private String config_database;
    private String config_database_url;
    private String config_database_user;
    private String config_database_pass;
    private String config_fileup_path;
    private String config_database_back;

    public int getConfig_id() {
        return config_id;
    }

    public void setConfig_id(int config_id) {
        this.config_id = config_id;
    }

    public String getConfig_database() {
        return config_database;
    }

    public void setConfig_database(String config_database) {
        this.config_database = config_database;
    }

    public String getConfig_database_url() {
        return config_database_url;
    }

    public void setConfig_database_url(String config_database_url) {
        this.config_database_url = config_database_url;
    }

    public String getConfig_database_user() {
        return config_database_user;
    }

    public void setConfig_database_user(String config_database_user) {
        this.config_database_user = config_database_user;
    }

    public String getConfig_database_pass() {
        return config_database_pass;
    }

    public void setConfig_database_pass(String config_database_pass) {
        this.config_database_pass = config_database_pass;
    }

    public String getConfig_fileup_path() {
        return config_fileup_path;
    }

    public void setConfig_fileup_path(String config_fileup_path) {
        this.config_fileup_path = config_fileup_path;
    }

    public String getConfig_database_back() {
        return config_database_back;
    }

    public void setConfig_database_back(String config_database_back) {
        this.config_database_back = config_database_back;
    }
}
